package commands;

/**
 * Интерфейс команды, которая может быть выполнена пользователем.
 * Каждая команда реализует данный интерфейс и определяет свою логику выполнения.
 */
public interface Command {
    /**
     * Выполняет команду с указанными аргументами.
     *
     * @param args аргументы команды (могут быть пустыми)
     */
    void execute(String[] args);

    /**
     * Возвращает краткое описание команды.
     *
     * @return описание команды для справки
     */
    String getDescription();
}
